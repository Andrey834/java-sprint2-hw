public class YearlyConstructor {
    public String monthYear;
    public int amountYear;
    public boolean isExpenseYear;

    public YearlyConstructor(String monthYear, int amountYear, boolean isExpenseYear) {
        this.monthYear = monthYear;
        this.amountYear = amountYear;
        this.isExpenseYear = isExpenseYear;
    }
}
